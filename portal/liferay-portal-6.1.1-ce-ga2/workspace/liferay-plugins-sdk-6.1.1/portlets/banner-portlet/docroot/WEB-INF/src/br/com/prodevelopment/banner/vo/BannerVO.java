package br.com.prodevelopment.banner.vo;

public class BannerVO {

	private String idBanner;
	private String imagem;
	private String linkDestino;
	private String titulo;
	private String exibeTitulo;
	private String dataCriacao;
	private String descricao;
	private String existeLink;

	public String getDataCriacao() {
		return dataCriacao;
	}

	public void setDataCriacao(String dataCriacao) {
		this.dataCriacao = dataCriacao;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public String getIdBanner() {
		return idBanner;
	}

	public void setIdBanner(String idBanner) {
		this.idBanner = idBanner;
	}

	public String getImagem() {
		return imagem;
	}

	public void setImagem(String imagem) {
		this.imagem = imagem;
	}

	public String getExibeTitulo() {
		return exibeTitulo;
	}

	public void setExibeTitulo(String exibeTitulo) {
		this.exibeTitulo = exibeTitulo;
	}

	public String getLinkDestino() {
		return linkDestino;
	}

	public void setLinkDestino(String linkDestino) {
		this.linkDestino = linkDestino;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getExisteLink() {
		return existeLink;
	}

	public void setExisteLink(String existeLink) {
		this.existeLink = existeLink;
	}


}
