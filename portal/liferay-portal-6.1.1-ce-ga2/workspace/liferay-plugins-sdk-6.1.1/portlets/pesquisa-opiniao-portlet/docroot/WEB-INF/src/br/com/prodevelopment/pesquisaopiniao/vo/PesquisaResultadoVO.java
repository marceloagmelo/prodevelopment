package br.com.prodevelopment.pesquisaopiniao.vo;

import java.util.List;

public class PesquisaResultadoVO {

	private String id;
	private String descricao;
	private String quantidadeInicial;
	private String quantidadeFinal;
	private PesquisaVO pesquisaVO;
	private List<RespostaVO> respostas;
	
	public PesquisaResultadoVO() {

		this.id = "";
		this.descricao = "";
		this.quantidadeInicial = "";
		this.quantidadeFinal = "";
		this.pesquisaVO = null;
		this.respostas = null;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public PesquisaVO getPesquisaVO() {
		return pesquisaVO;
	}

	public void setPesquisaVO(PesquisaVO pesquisaVO) {
		this.pesquisaVO = pesquisaVO;
	}

	public List<RespostaVO> getRespostas() {
		return respostas;
	}

	public void setRespostas(List<RespostaVO> respostas) {
		this.respostas = respostas;
	}

	public String getQuantidadeInicial() {
		return quantidadeInicial;
	}

	public void setQuantidadeInicial(String quantidadeInicial) {
		this.quantidadeInicial = quantidadeInicial;
	}

	public String getQuantidadeFinal() {
		return quantidadeFinal;
	}

	public void setQuantidadeFinal(String quantidadeFinal) {
		this.quantidadeFinal = quantidadeFinal;
	}

}
