package br.com.prodevelopment.pesquisaopiniao.vo;

public class RespostaUsuarioVO {

	private String id;
	private PesquisaUsuarioVO pesquisaUsuarioVO;
	private RespostaVO respostaVO;
	
	public RespostaUsuarioVO() {
		this.id = "";
		this.pesquisaUsuarioVO = null;
		this.respostaVO = null;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public PesquisaUsuarioVO getPesquisaUsuarioVO() {
		return pesquisaUsuarioVO;
	}

	public void setPesquisaUsuarioVO(PesquisaUsuarioVO pesquisaUsuarioVO) {
		this.pesquisaUsuarioVO = pesquisaUsuarioVO;
	}

	public RespostaVO getRespostaVO() {
		return respostaVO;
	}

	public void setRespostaVO(RespostaVO respostaVO) {
		this.respostaVO = respostaVO;
	}
}
