package br.com.prodevelopment.testeconhecimento.vo;

import java.util.List;

public class TesteUsuarioVO {

	private String id;
	private String nome;
	private String email;
	private TesteVO testeVO;
	private List<RespostaUsuarioVO> respostas;
	private String total;
	private String imagemUrl;
	private String idUsuario;
	private String totalRespostaCerta;
	private String totalRespostaErrada;
	private String totalPontuacao;
	
	public TesteUsuarioVO() {
		this.id = "";
		this.nome = "";
		this.email = "";
		this.testeVO = null;
		this.respostas = null;
		this.total = "";
		this.imagemUrl = "";
		this.idUsuario = "";
		this.totalRespostaCerta = "";
		this.totalRespostaErrada = "";
		this.totalPontuacao = "";
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public TesteVO getTesteVO() {
		return testeVO;
	}

	public void setTesteVO(TesteVO testeVO) {
		this.testeVO = testeVO;
	}

	public List<RespostaUsuarioVO> getRespostas() {
		return respostas;
	}

	public void setRespostas(List<RespostaUsuarioVO> respostas) {
		this.respostas = respostas;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getImagemUrl() {
		return imagemUrl;
	}

	public void setImagemUrl(String imagemUrl) {
		this.imagemUrl = imagemUrl;
	}

	public String getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTotalRespostaCerta() {
		return totalRespostaCerta;
	}

	public void setTotalRespostaCerta(String totalRespostaCerta) {
		this.totalRespostaCerta = totalRespostaCerta;
	}

	public String getTotalRespostaErrada() {
		return totalRespostaErrada;
	}

	public void setTotalRespostaErrada(String totalRespostaErrada) {
		this.totalRespostaErrada = totalRespostaErrada;
	}

	public String getTotalPontuacao() {
		return totalPontuacao;
	}

	public void setTotalPontuacao(String totalPontuacao) {
		this.totalPontuacao = totalPontuacao;
	}
	
	
}
